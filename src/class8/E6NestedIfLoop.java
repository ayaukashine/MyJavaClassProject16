package class8;

public class E6NestedIfLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.print(i+ " ");

        }
        System.out.println();
        for (int i = 0; i < 5; i++) {
            System.out.print(i+ " ");

        }
        System.out.println();
        for (int i = 0; i < 5; i++) {
            System.out.print(i+ " ");

        }

    }
}
