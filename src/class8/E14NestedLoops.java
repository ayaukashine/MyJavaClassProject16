package class8;

public class E14NestedLoops {
    public static void main(String[] args) {


        for (int i = 1; i < 4; i++) {
            for (int j = 0; j < 2; j++) {
                System.out.print(i+j+" ");
            j++;
            }
            System.out.println( );
        }
    }
}
